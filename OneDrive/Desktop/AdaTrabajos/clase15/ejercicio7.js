//⦁	Ejercicio 6: Buscar la última posición de un elemento repetido
//Tienes una lista de números [2, 5, 7, 2, 8, 7]. //Encuentra //y muestra la última posición en la que aparece el número 7.


const numeros = [2, 5, 7, 2, 8, 7];
const numeroBuscado = 7;    
const ultimaPosicion = numeros.lastIndexOf(numeroBuscado);

console.log(`La última posición del número ${numeroBuscado} es: ${ultimaPosicion}`);        


        

